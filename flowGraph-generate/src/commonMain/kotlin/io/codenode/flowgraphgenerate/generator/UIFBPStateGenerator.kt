 * Emits the public immutable `data class {Name}State` (FR-001). One `val`
 * property per `spec.sinkInputs` entry, in declared order, with default
 * values per the contract's table. The prior singleton-object emission is
 * gone; per-flow-graph runtime state lives in {Name}Runtime's closure
 * (Decision 8). The State data class is the genuine SSOT for the UI.
 *
